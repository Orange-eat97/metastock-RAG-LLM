from __future__ import annotations

import re
from typing import Any


"""
Validator for generated MetaStock Explorer JSON.

Scope:
- This validator is deliberately conservative.
- It checks generated Explorer code fields, not trading correctness.
- It should allow functions that exist in the current knowledge base, while
  still catching common LLM mistakes such as natural-language formulas,
  undefined ColX references, programming operators, and future references.

Important:
- The generator prompt already says explorer_code_body may use direct formulas.
- ColA/ColB references are allowed in explorer_code_body only when the matching
  column is defined.
- Historical references should be placed inside column formulas, not around ColA.
"""


